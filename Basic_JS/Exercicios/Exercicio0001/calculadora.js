primeiroValor = parseFloat(prompt("Digite o Primeiro valor: "))
segundoValor = parseFloat(prompt("Digite o Segundo Valor: "))

soma = primeiroValor + segundoValor;
divisao = primeiroValor / segundoValor;
subtracao = primeiroValor - segundoValor;
multiplicacao = primeiroValor * segundoValor;

alert(
    "Aqui estão os resultados:\n " +
    "\nO valor da Soma é :" + soma +
    "\nO valor da Divisão é: " + divisao +
    "\nO valor da Subtração é: " + subtracao +
    "\nO valor da Multiplicação é: " + multiplicacao
)
